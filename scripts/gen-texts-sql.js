// Generates an idempotent SQL file that upserts ALL site texts from the bundled
// translations (src/translations/en.json + uk.json) into the `site_texts` table.
// Run: node scripts/gen-texts-sql.js  -> writes scripts/seed-all-texts.sql
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');

function flatten(obj, prefix = '') {
  const out = [];
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === 'object') {
      out.push(...flatten(v, key));
    } else if (typeof v === 'string') {
      out.push({ key, value: v, group: key.split('.')[0] });
    }
  }
  return out;
}

function sqlStr(s) {
  return `'${String(s).replace(/'/g, "''")}'`;
}

const en = JSON.parse(fs.readFileSync(path.join(ROOT, 'src/translations/en.json'), 'utf8'));
const uk = JSON.parse(fs.readFileSync(path.join(ROOT, 'src/translations/uk.json'), 'utf8'));

const enFlat = flatten(en);
const ukMap = new Map(flatten(uk).map((r) => [r.key, r.value]));

const lines = enFlat.map((r) => {
  const valueUk = ukMap.has(r.key) ? ukMap.get(r.key) : r.value;
  return `  (${sqlStr(r.key)}, ${sqlStr(r.group)}, ${sqlStr(r.value)}, ${sqlStr(valueUk)}, ${sqlStr('Auto-imported from site translations')})`;
});

const sql = `-- =============================================================================
-- Seed ALL site texts from bundled translations (en.json + uk.json)
-- Generated by scripts/gen-texts-sql.js -- ${enFlat.length} keys
-- Idempotent: re-running updates EN/UK/description for existing keys.
-- Run in Supabase SQL Editor.
-- =============================================================================

insert into site_texts (key, "group", value_en, value_uk, description)
values
${lines.join(',\n')}
on conflict (key) do update set
  value_en    = excluded.value_en,
  value_uk    = excluded.value_uk,
  "group"     = excluded."group";
`;

const outPath = path.join(ROOT, 'scripts', 'seed-all-texts.sql');
fs.writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote ${outPath} with ${enFlat.length} keys`);
